# convertendo inteiros para float (flutuantes ou nuumeros quebrados)

preco = 10   # declarando a variável
print(preco) # mostrando o valor armazenado na variável

print(preco / 2) # uma barra (/) apresenta o valor dividido como float 

print(preco // 2) # duas barras (//) apresenta o valor dividido como inteiro


